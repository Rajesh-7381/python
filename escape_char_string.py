# doublequote
txt="i am \"rajesh\" kumar swain"
print(txt)

# single quote
txt='i am \'rajesh kumar swain'
print(txt)

# backlash
txt="i am rajesh\\kumar swain"
print(txt)

# new line
txt="i am \nrajesh kumar swain"
print(txt)

#carriage return
txt="i am \rrajesh kumar swain"
print(txt)

#tab
txt="i am \trajesh kumar swain"
print(txt)
 
#backspace
txt="i am \brajesh kumar swain"
print(txt)


